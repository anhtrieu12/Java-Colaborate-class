package ControlFlow.IF_Block.LoopsinJava.Forloop.DoWhileLoop;

public class DoWhileLoop_Intro {
    //Do While loop will always execute at least one time.

    public static void main(String[] args) {

        int i = 0;

        do {
            System.out.println(i);
            i++;
        } while (i < 0);


        int j = 4;

        do {
            System.out.println(j);
            j++;
        } while (j < 3);


    }
}
