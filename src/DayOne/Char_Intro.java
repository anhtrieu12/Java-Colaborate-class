package DayOne;

public class Char_Intro {

    public static void main(String[] args) {

        char ch1 = 'Z';
        System.out.println(ch1);

        char ch2 = 536; //536 stand for dollar sign
        System.out.println(ch2);

        // for loop

        for(int i = 32; i < 127; i++){
            System.out.println((char)i);
        }

    }
}
