package DayOne;

public class DataTypes {

    public static void main(String[] args) {
     // Primitive data types
        boolean b1 = true;
        System.out.println(b1);

        byte a = 124;
        System.out.println(a);

        char c1 = 'a';
        System.out.println(c1);

        // We can use number stand for character
        char c2 = 66;
        System.out.println(c2);

        short s1 = 32000;
        System.out.println(s1);

        long l1 = 98000;

        float f1 = 6.983245f;
        double d1 = 6.9876543210;


        //non-primitive data types

        String str = "Test";
        System.out.println(str);

        String strOne = "90909@$";

        String strNumOne = "10";
        String strNumTwo = "10";

        System.out.println(strNumOne + strNumTwo);



    }

}
